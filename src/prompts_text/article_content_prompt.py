from typing import Dict, List, Any

def create_h3_content_prompt(main_keyword: str, h3_to_write: str, relevant_info: str) -> str:
    """
    【改善版】
    指定されたH3見出しと、それに関連する情報のみを元に、高品質な会話形式の本文を生成するプロンプト。
    """
    prompt = f"""
# 指示
あなたは「{main_keyword}」というテーマを扱うプロのWEBライターです。
以下の条件に従って、読者にとって非常に分かりやすく魅力的なブログ記事の本文を作成してください。

## 執筆する見出し
### {h3_to_write}

## 参考情報
- 以下の情報は、この記事の信頼性を担保する重要なデータです。
- この情報のみを元にして、内容を膨らませて執筆してください。
- あなた自身の知識や、参考情報にない内容は絶対に追加しないでください。
```json
{relevant_info}
```

## 執筆ルール
- **形式:** 困っている女性客と、親切な専門家である店員の「会話形式」で執筆してください。
- **雰囲気:** 読者が楽しく、明るい気持ちで読めるような、親しみやすい雰囲気でお願いします。
- **内容:** 専門用語を避け、初心者にも理解できるように具体例を交えて解説してください。メリットだけでなく、デメリットや注意点にも触れることで、信頼性を高めてください。
- **構成:** PREP法（結論→理由→具体例）を意識し、論理的で分かりやすい文章構成にしてください。
- **文字数:** 400〜500文字程度でお願いします。
- **出力:** **会話形式の本文のみ**を生成してください。見出し、前置き、後書きは一切不要です。

上記ルールを厳格に守り、指定された見出しに対する完璧な本文を完成させてください。
    """
    return prompt
